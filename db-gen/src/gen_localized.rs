        out.push_str("        out.entry(row.field.clone())\n");
        out.push_str("            .or_default()\n");
        out.push_str("            .entry(row.owner_id)\n");
        out.push_str("            .or_default()\n");
        out.push_str("            .insert(row.locale.clone(), row.value.clone());\n");
