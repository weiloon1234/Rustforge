        out.push_str("            .where_locale(Op::Eq, locale.clone())\n");
        out.push_str("            .first()\n");
        out.push_str("            .await?;\n");
        out.push_str("        if let Some(current) = current {\n");
        out.push_str("            LocalizedModel::new(db.clone(), None)\n");
        out.push_str("                .update()\n");
        out.push_str("                .where_id(Op::Eq, current.id)\n");
        out.push_str("                .set_value(value.clone())\n");
        out.push_str("                .save()\n");
        out.push_str("                .await?;\n");
        out.push_str("        } else {\n");
        out.push_str("            LocalizedModel::new(db.clone(), None)\n");
        out.push_str("                .insert()\n");
        out.push_str("                .set_id(generate_snowflake_i64())\n");
        out.push_str("                .set_owner_type(owner_type.to_string())\n");
        out.push_str("                .set_owner_id(owner_id)\n");
        out.push_str("                .set_field(field.to_string())\n");
        out.push_str("                .set_locale(locale.clone())\n");
